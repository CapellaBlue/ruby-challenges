the_weather = "sunny"
case the_weather
when 'sunny'
puts "Oh, sweet-- go bask in that beautiful sun! Don't forget your sunglasses!!"
when 'rainy'
puts "So typical of Seattle.  Boots again. Yay!"
when 'snowing'
puts "This is so weird! Quick, go put on snow pants! Igloo building time!"
when 'cloudy'
puts "I wonder if it will rain.  Better bring our raincoat just in case."
end
